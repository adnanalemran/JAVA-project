package com.example.datatype;

public class Main {
    public static void main(String[] args) {

        boolean b;
        char c;
        short s = 434;
        int i = 30; // dynamic init
        float f = 10.2f;
        double d = 10.3;
        b = true;
        System.out.println("b is " + b);
        c = 'a';
        System.out.println("c is " + c);
        System.out.println("i is " + i);
        System.out.println(s +"\n"+ f +"\n"+ d);
    }
}